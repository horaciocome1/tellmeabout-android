@OptIn(ExperimentalMaterial3Api::class, ExperimentalComposeUiApi::class)
@RootNavGraph(start = true)
@Destination
@Composable
fun EnterTopicScreen(
    navigator: DestinationsNavigator,
    viewModel: EnterTopicScreenViewModel = hiltViewModel(),
) {
    val loading by viewModel.loading.collectAsStateWithLifecycle()
    val error by viewModel.error.collectAsStateWithLifecycle()
    val factsGenerated by viewModel.factsGenerated.collectAsStateWithLifecycle(initialValue = false)

    val focusManager = LocalFocusManager.current

    LaunchedEffect(factsGenerated) {
        if (factsGenerated) {
            navigator.navigate(FactsScreenDestination(viewModel.topic))
        }
    }

    LaunchedEffect(error) {
        // TODO: show error
    }

    Box(
        modifier = Modifier
            .fillMaxSize()
            .background(color = MaterialTheme.colorScheme.surface)
            .pointerInteropFilter {
                focusManager.clearFocus()
                false
            },
    ) {
        Surface(
            modifier = Modifier
                .align(Alignment.Center)
                .fillMaxSize(0.9f),
            shape = MaterialTheme.shapes.large,
            color = MaterialTheme.colorScheme.primary.copy(
                alpha = 0.05f,
            ),
        ) {
            Column(
                modifier = Modifier
                    .fillMaxSize(),
                horizontalAlignment = Alignment.CenterHorizontally,
                verticalArrangement = Arrangement.Center,
            ) {
                Text(
                    text = "Tell me\nabout...",
                    style = MaterialTheme.typography.headlineLarge.copy(
                        color = MaterialTheme.colorScheme.primary,
                    ),
                    modifier = Modifier
                        .align(Alignment.Start)
                        .padding(horizontal = 32.dp),
                )
                OutlinedTextField(
                    value = viewModel.topic,
                    onValueChange = viewModel::onTopicChanged,
                    singleLine = true,
                    label = {
                        Text(
                            text = "Topic",
                            style = MaterialTheme.typography.bodyLarge.copy(
                                color = MaterialTheme.colorScheme.primary.copy(
                                    alpha = 0.5f,
                                ),
                            ),
                        )
                    },
                    supportingText = {
                        val annotatedString = buildAnnotatedString {
                            append("Enter a topic to get started. ")
                            append("For example, '")
                            withStyle(
                                style = SpanStyle(
                                    color = MaterialTheme.colorScheme.primary.copy(
                                        alpha = 0.5f,
                                    ),
                                ),
                            ) {
                                append("cats")
                            }
                            append("' or '")
                            withStyle(
                                style = SpanStyle(
                                    color = MaterialTheme.colorScheme.primary.copy(
                                        alpha = 0.5f,
                                    ),
                                ),
                            ) {
                                append("african families")
                            }
                            append("'.")
                        }
                        Text(
                            text = annotatedString,
                            style = MaterialTheme.typography.labelLarge.copy(
                                color = MaterialTheme.colorScheme.primary.copy(
                                    alpha = 0.3f,
                                ),
                            ),
                        )
                    },
                    suffix = {
                        Text(
                            text = "${viewModel.topic.length}/$MAX_TOPIC_LENGTH",
                            style = MaterialTheme.typography.labelSmall.copy(
                                color = MaterialTheme.colorScheme.primary.copy(
                                    alpha = 0.3f,
                                ),
                            ),
                        )
                    },
                    textStyle = MaterialTheme.typography.bodyLarge.copy(
                        color = MaterialTheme.colorScheme.primary,
                    ),
                    shape = MaterialTheme.shapes.small,
                    colors = TextFieldDefaults.outlinedTextFieldColors(
                        unfocusedBorderColor = MaterialTheme.colorScheme.primary.copy(
                            alpha = 0.3f,
                        ),
                    ),
                    enabled = !loading,
                    keyboardOptions = KeyboardOptions(
                        imeAction = ImeAction.Go,
                    ),
                    keyboardActions = KeyboardActions(
                        onGo = {
                            viewModel.generateFacts()
                            focusManager.clearFocus()
                        },
                    ),
                    modifier = Modifier
                        .fillMaxWidth()
                        .padding(
                            start = 24.dp,
                            top = 16.dp,
                            end = 24.dp,
                        ),
                )
                Spacer(modifier = Modifier.height(100.dp))
                Button(
                    onClick = viewModel::generateFacts,
                    enabled = !loading && viewModel.topic.isNotBlank(),
                ) {
                    Text(
                        text = "Get Started",
                        style = MaterialTheme.typography.bodyLarge,
                    )
                    AnimatedVisibility(visible = loading) {
                        CircularProgressIndicator(
                            modifier = Modifier
                                .height(16.dp)
                                .width(32.dp)
                                .padding(start = 16.dp)
                                .align(Alignment.CenterVertically),
                            color = MaterialTheme.colorScheme.primary,
                            strokeWidth = 2.dp,
                        )
                    }
                }
            }
        }
    }
}

